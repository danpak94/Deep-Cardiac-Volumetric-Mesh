def apply_vtkSmoothPolyDataFilter(verts, faces, n_iter=100, relaxation_factor=0.01):
    '''
    http://www.vtk.org/doc/nightly/html/classvtkSmoothPolyDataFilter.html
    '''

    def mkVtkIdList(it):
        """
        :param it: A python iterable.
        :return: A vtkIdList
        """
        vil = vtk.vtkIdList()
        for i in it:
            vil.InsertNextId(int(i))
        return vil

    # converting verts and faces to vtkPolyData object
    vtk_mesh = vtk.vtkPolyData()
    points = vtk.vtkPoints()
    polys = vtk.vtkCellArray()

    for idx, vert in enumerate(verts):
        points.InsertPoint(idx, vert)
    for face in faces:
        polys.InsertNextCell(mkVtkIdList(face))

    vtk_mesh.SetPoints(points)
    vtk_mesh.SetPolys(polys)

    # setting and applying the filter
    smooth_filter = vtk.vtkSmoothPolyDataFilter()
    smooth_filter.SetInputData(vtk_mesh)
    smooth_filter.SetNumberOfIterations(n_iter)
    smooth_filter.SetRelaxationFactor(relaxation_factor)
    smooth_filter.FeatureEdgeSmoothingOff()
    # smooth_filter.FeatureEdgeSmoothingOn()
    smooth_filter.BoundarySmoothingOff()
    # smooth_filter.BoundarySmoothingOn()
    smooth_filter.Update()
    vtk_mesh_smooth = smooth_filter.GetOutput()

    # converting vtkPolyData object back to verts and faces
    verts_smooth = vtk.util.numpy_support.vtk_to_numpy(vtk_mesh_smooth.GetPoints().GetData())
    faces_smooth = vtk.util.numpy_support.vtk_to_numpy(vtk_mesh_smooth.GetPolys().GetData()).reshape(-1, 4)[:, 1:]

    return verts_smooth, faces_smooth

def get_top_commissure_cut_vert(aw_verts, aw_faces, border_verts, commissure_pt, cut_path_pts):
    # from path_pts, get smallest dist pts to top or bottom surface of aortic wall
    border_cover_faces = igl.polygon_mesh_to_triangle_mesh(np.arange(border_verts.shape[0])[np.newaxis])
    border_cover_mesh = utils_sp.mesh_to_pyvista_PolyData(border_verts, border_cover_faces)
    smallest_dists = igl.point_mesh_squared_distance(cut_path_pts, *get_verts_faces_from_pyvista(border_cover_mesh))[0]

    # 2 smallest dist points candidates b/c of how we defined cutting plane - choose one closest in geo dist to commissure pt
    commissure_top_candidates = cut_path_pts[np.argpartition(smallest_dists, 2)[:2]] # get two smallest values
    idxes_src = get_min_dist_verts(border_verts, commissure_pt[np.newaxis], return_idx=True)[1]
    idxes_tgt = get_min_dist_verts(border_verts, commissure_top_candidates, return_idx=True)[1]
    dists = igl.exact_geodesic(aw_verts, aw_faces, np.array(idxes_src), np.array(idxes_tgt))
    top_commissure_cut_vert = commissure_top_candidates[np.argmin(dists)]

    return top_commissure_cut_vert

def split_list(input_list, split_idxes):
    new_list = []
    prev_idx = 0
    for split_idx in split_idxes:
        new_list.append(input_list[prev_idx:split_idx])
        prev_idx = split_idx
    return new_list

def get_sep_components_from_all_verts_all_faces(all_verts, all_faces, idx_tracks, verts_len_list, faces_len_list):
    """ inputs from: _,_,idx_tracks = igl.remove_duplicates(all_verts, all_faces) """
    idx_track_list = np.split(idx_tracks, np.cumsum(verts_len_list)[:-1]) # shape [9141, 985, 985, 985]
    verts_new_list = [all_verts[idx_track] for idx_track in idx_track_list]

    faces_new_list = []
    faces_list = split_list(all_faces, np.cumsum(faces_len_list))
    cum_faces_len = 0
    for faces, idx_track in zip(faces_list, idx_track_list):
        replace_dict = {k: v for k, v in zip(idx_track, cum_faces_len + np.arange(len(idx_track)))}
        faces_new = replace_face_idxes_with_dict(faces, replace_dict)
        faces_new_list.append([[idx - cum_faces_len for idx in face_new] for face_new in faces_new])
        # faces_new_list.append(faces_new - cum_faces_len) # only works for np, not list of lists for mixed tri/quad elements
        cum_faces_len += len(faces)

    return verts_new_list, faces_new_list

def get_sep_components_from_all_verts_all_faces_torch(all_verts, all_faces, idx_tracks, verts_len_list, faces_len_list):
    """
    inputs from: _,_,idx_tracks = igl.remove_duplicates(all_verts, all_faces)
    all_verts: torch.tensor, [n_batch, n_verts, n_dim]
    all_faces: torch.tensor, [n_batch, n_faces (3), n_verts_per_face]
    """

    def size_splits(tensor, split_sizes, dim=0):
        """Splits the tensor according to chunks of split_sizes.

        Arguments:
            tensor (Tensor): tensor to split.
            split_sizes (list(int)): sizes of chunks
            dim (int): dimension along which to split the tensor.
        """
        if dim < 0:
            dim += tensor.dim()

        dim_size = tensor.size(dim)
        if dim_size != torch.sum(torch.Tensor(split_sizes)):
            raise KeyError("Sum of split sizes exceeds tensor dim")

        splits = torch.cumsum(torch.Tensor([0] + split_sizes), dim=0)[:-1]

        return list(tensor.narrow(int(dim), int(start), int(length))
            for start, length in zip(splits, split_sizes))

    idx_track_list = size_splits(torch.tensor(idx_tracks, dtype=int), verts_len_list) # e.g. shape [9141, 985, 985, 985]
    verts_new_list = [all_verts[:,idx_track,:] for idx_track in idx_track_list]

    faces_split_list = size_splits(all_faces, faces_len_list, dim=1)
    face_idx_offset_list = [0, *(list(np.cumsum(verts_len_list))[:-1])]

    faces_new_list = []
    cum_faces_len = 0
    for faces, idx_track in zip(faces_split_list, idx_track_list):
        replace_dict = {k: v for k, v in zip(idx_track, cum_faces_len + np.arange(len(idx_track)))}
        faces_new = replace_face_idxes_with_dict_torch(faces, replace_dict)
        faces_new_list.append(faces_new - cum_faces_len) # only works for np, not list of lists for mixed tri/quad elements
        cum_faces_len += len(faces)

    # remove batch dimension (assuming n_batch=1)
    output_verts_new_list = [verts_new.squeeze(0) for verts_new in verts_new_list]
    output_faces_new_list = [faces_new.squeeze(0) for faces_new in faces_new_list]

    return output_verts_new_list, output_faces_new_list

def find_neighbor_nodes(faces, node_idx):
    """Pass the index of the node in question.
    Returns the vertex indices of the vertices connected with that node."""
    neighbor_faces = np.vstack([face for face in faces if node_idx in face])
    connected = np.unique(neighbor_faces.ravel()) # get unqiue node indices
    return np.delete(connected, np.argwhere(connected == node_idx)) # delete original node_idx

def find_neighbor_face_idxes_by_edge(faces, edge):
    return np.where(np.all(faces == edge[0], faces == edge[1]))

def replace_face_idxes_with_dict(faces, replace_dict):
    if isinstance(faces, list):
        faces_new = []
        for face in faces:
            new_face = [replace_dict[orig_idx] for orig_idx in face]
            faces_new.append(new_face)
    elif isinstance(faces, torch.Tensor):
        faces_new = faces.clone()
        for key, val in replace_dict.items():
            faces_new[faces==key] = int(val)
    elif isinstance(faces, np.ndarray):
        faces_new = faces.copy()
        for key, val in replace_dict.items():
            faces_new[faces==key] = int(val)
    return faces_new

def replace_face_idxes_with_dict_torch(faces, replace_dict):
    faces_new = faces.clone()
    for key, val in replace_dict.items():
        faces_new[faces==key] = int(val)
    return faces_new

def load_inp_as_pv(filepath, return_tri_pv=False):
    store_verts = False
    store_faces_2d = False
    store_faces_3d = False

    with open(filepath, 'r') as fstream:
        verts_dict = {}
        faces_2d_list = []
        faces_3d_list = []

        line_hold = ''

        for line_idx, line_orig in enumerate(fstream):
            line = line_orig.rstrip('\n')

            """ this is to take care of long face_idxes, sometimes there is \n after some amount of indices """
            if line[-1] == ',':
                line_hold = line
                continue
            else:
                line = line_hold + line
                line_hold = ''

            line_parse = line.split('*')

            if len(line_parse) > 1:
                if line_parse[1] == 'NODE':
                    store_verts = True
                    continue

                if 'ELEMENT' in line_parse[1]:
                    if line.split(',')[1] == 'TYPE=S3':
                        store_faces_2d = True
                        n_elem = 3
                        continue

                    elif line.split(',')[1] == 'TYPE=S4R' or line.split(',')[1] == 'TYPE=S4':
                        store_faces_2d = True
                        n_elem = 4
                        continue

                    elif line.split(',')[1] == 'TYPE=C3D8R':
                        store_faces_3d = True
                        n_elem = 8
                        continue

            if store_verts:
                if len(line_parse) == 1: # to turn off store_verts when we hit another asterisk
                    line_parse2 = line.split(',  ')
                    """
                    line_parse2[0]: vert index given by .inp
                    we want to replace those with our own index - just the order of vertex listing np.arange(len)
                    fixed indexing line_parse2[1:4] b/c always 3D points
                    """
                    verts_dict[int(line_parse2[0])] = line_parse2[1:4]
                else:
                    store_verts = False

            if store_faces_2d: # to turn off store_faces_2d when we hit another asterisk
                if len(line_parse) == 1:
                    line_parse2 = line.split(',     ')
                    faces_2d_list.append(np.array(line_parse2[1 : n_elem+1]).astype(int).tolist())
                else:
                    store_faces_2d = False

            if store_faces_3d:
                if len(line_parse) == 1: # to turn off store_faces_3d when we hit another asterisk
                    line_parse2 = line.split(',     ')
                    faces_3d_list.append(np.array(line_parse2[1 : n_elem+1]).astype(int).tolist())
                else:
                    store_faces_3d = False

    """ we want to get rid of verts that don't contribute to faces (probably free-floating nodes in hypermesh) """
    all_face_idxes = np.unique(np.hstack(faces_2d_list + faces_3d_list))
    verts_for_faces_dict = {key: value for key, value in verts_dict.items() if key in all_face_idxes}

    """ replace .inp indices with vertex order indices """
    replace_dict = {idx1: idx2 for idx1, idx2 in zip(list(verts_for_faces_dict.keys()), np.arange(len(verts_for_faces_dict)))}
    faces_2d_idx_changed = replace_face_idxes_with_dict(faces_2d_list, replace_dict)
    faces_3d_idx_changed = replace_face_idxes_with_dict(faces_3d_list, replace_dict)

    if len(faces_2d_idx_changed) > 0:
        mesh_2d_pv = utils_sp.mesh_to_pyvista_PolyData(np.array(list(verts_for_faces_dict.values())).astype(float), faces_2d_idx_changed)
    else:
        mesh_2d_pv = None

    if len(faces_3d_idx_changed) > 0:
        mesh_3d_pv = mesh_to_pyvista_UnstructuredGrid(np.array(list(verts_for_faces_dict.values())).astype(float), faces_3d_idx_changed)
    else:
        mesh_3d_pv = None

    if mesh_2d_pv and mesh_3d_pv:
        return mesh_2d_pv, mesh_3d_pv
    elif mesh_2d_pv:
        if return_tri_pv:
            tri_faces = split_quad_to_2_tri_mesh(faces_2d_idx_changed)
            mesh_2d_pv_tri = utils_sp.mesh_to_pyvista_PolyData(mesh_2d_pv.points, tri_faces)
            return mesh_2d_pv, mesh_2d_pv_tri
        else: mesh_2d_pv
    elif mesh_3d_pv:
        return mesh_3d_pv
    else:
        return None

def extract_single_surf_from_3d_elem(mesh_3d_pv, base_surf=True):
    verts, faces_3d = get_verts_faces_from_pyvista(mesh_3d_pv)
    if base_surf:
        face_surf = faces_3d[:,:4]
    else:
        face_surf = faces_3d[:,4:]

    verts_dict = {idx: vert for idx, vert in enumerate(verts)}

    """ remove verts that don't contribute to surface faces and update face_idxes with new vert indices """
    all_face_idxes = np.unique(np.hstack(face_surf))
    verts_for_faces_dict = {key: value for key, value in verts_dict.items() if key in all_face_idxes}

    replace_dict = {idx1: idx2 for idx1, idx2 in zip(list(verts_for_faces_dict.keys()), np.arange(len(verts_for_faces_dict)))}
    faces_idx_changed = replace_face_idxes_with_dict(face_surf, replace_dict)

    mesh_2d_pv = utils_sp.mesh_to_pyvista_PolyData(np.array(list(verts_for_faces_dict.values())).astype(float), faces_idx_changed)

    return mesh_2d_pv

def remove_duplicates_DP(verts, faces, eps):
    """main difference with igl.remove_duplicates is just that I need flexibility with mixed tri/quad meshes"""
    faces_tri = split_quad_to_2_tri_mesh(faces)
    new_verts, _, idx_tracks = igl.remove_duplicates(verts, faces_tri, eps)
    replace_dict = {k: v for k, v in zip(np.arange(len(idx_tracks)), idx_tracks)}
    new_faces = replace_face_idxes_with_dict(faces, replace_dict)

    return new_verts, new_faces, idx_tracks

def mesh_to_pyvista_UnstructuredGrid(verts, faces):
    """ only compatible with tetrahedron, hexahedron (8 verts, flexible rectangular prism) and wedge (6 verts, flexible triangular prism) for now """
    faces_pv = []
    cell_types = []
    for face in faces:
        faces_pv += ([len(face)] + list(face))
        if len(face) == 4:
            cell_types.append(vtk.VTK_TETRA)
        elif len(face) == 6:
            cell_types.append(vtk.VTK_WEDGE)
        elif len(face) == 8:
            cell_types.append(vtk.VTK_HEXAHEDRON)
        elif len(face) == 15:
            cell_types.append(vtk.VTK_QUADRATIC_WEDGE)
        elif len(face) == 20:
            cell_types.append(vtk.VTK_QUADRATIC_HEXAHEDRON)
        else:
            raise ValueError('faces must have either 4, 6, or 8 indices for each cell')

    faces_pv = np.array(faces_pv).astype(int)
    cell_types = np.array(cell_types).astype(np.uint8)

    return pv.UnstructuredGrid(faces_pv, cell_types, verts)

