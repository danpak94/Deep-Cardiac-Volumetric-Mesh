def get_clipper_along_points(verts, faces, points, cyclical_clipper=False, return_faces_flipped=False):
    '''
    Note: points will be projected onto mesh surface, so they don't have to reside on the mesh already
    '''
    # get projections of points onto mesh
    _, face_idxes, proj_pts = igl.point_mesh_squared_distance(points, verts, faces)
    # print(proj_pts_old.shape)
    # # remove proj_pts that are too close to each other
    # new_pts, _, _ = igl.remove_duplicates(proj_pts_old,
    #                                       igl.polygon_mesh_to_triangle_mesh(np.arange(proj_pts_old.shape[0])[np.newaxis]),
    #                                       5e-2) # same val as scale for now
    # # get projections of new projc_pts (no duplicates) onto mesh
    # _, face_idxes, proj_pts = igl.point_mesh_squared_distance(new_pts, verts, faces)
    # print(proj_pts.shape)

    scale = 0.05
    if not cyclical_clipper:
        # extending border verts to properly clip (cgal requires clipper to stick out of mesh that we're trying to cut)
        proj_pts[0] = proj_pts[0] + scale*(proj_pts[0]-proj_pts[1])/np.linalg.norm(proj_pts[0]-proj_pts[1])
        proj_pts[-1] = proj_pts[-1] + scale*(proj_pts[-1]-proj_pts[-2])/np.linalg.norm(proj_pts[-1]-proj_pts[-2])

    mesh_pv = utils_sp.mesh_to_pyvista_PolyData(verts, faces)
    proj_pts_offset1 = proj_pts + mesh_pv.cell_normals[face_idxes]*scale
    proj_pts_offset2 = proj_pts - mesh_pv.cell_normals[face_idxes]*scale
    cut_verts = np.concatenate([proj_pts_offset1, proj_pts_offset2])    
    
    cut_faces = [[i, i+1, len(proj_pts)+i+1, len(proj_pts)+i] for i in range(len(proj_pts)-1)]
    
    if cyclical_clipper:
        # filling in gap between last verts and first verts
        cut_faces.append([len(proj_pts)-1, 0, len(proj_pts), len(proj_pts)*2-1])
    
    cut_faces = np.array(cut_faces)

    # cut_verts, cut_faces, _ = igl.remove_duplicates(cut_verts, cut_faces, 1e-2)

    if return_faces_flipped:
        cut_faces_flipped = cut_faces.copy()
        cut_faces_flipped[:,1] = cut_faces[:,3]
        cut_faces_flipped[:,3] = cut_faces[:,1]
        
        cut_faces = igl.polygon_mesh_to_triangle_mesh(cut_faces)
        cut_faces_flipped = igl.polygon_mesh_to_triangle_mesh(cut_faces_flipped)

        return cut_verts, cut_faces, cut_faces_flipped
    else:
        cut_faces = igl.polygon_mesh_to_triangle_mesh(cut_faces)
        return cut_verts, cut_faces

