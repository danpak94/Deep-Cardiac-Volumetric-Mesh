def get_verts_faces_from_pyvista(custom_pv):
    verts = custom_pv.points

    if isinstance(custom_pv, pv.core.pointset.PolyData):
        faces_pv = custom_pv.faces
    elif isinstance(custom_pv, pv.core.pointset.UnstructuredGrid):
        faces_pv = custom_pv.cells

    """ faces_pv is in vtk format
    e.g. [3,100,101,102,4,202,203,204,205] is tri element [100,101,102] and quad element [202,203,204,205]
    """
    faces = []
    is_n_verts = True
    for val in faces_pv:
        if is_n_verts:
            n_verts_remain = val
            face = []
            is_n_verts = False
            continue

        if n_verts_remain > 0:
            face.append(val)
            n_verts_remain -= 1

        if n_verts_remain == 0:
            faces.append(face)
            is_n_verts = True

    n_elem = np.unique(np.array([len(face) for face in faces]))
    if len(n_elem) == 1:
        faces = np.array(faces)

    # if isinstance(custom_pv, pv.core.pointset.PolyData):
    #     faces = custom_pv.faces.reshape(-1, custom_pv.faces[0]+1)[:,1:]
    # elif isinstance(custom_pv, pv.core.pointset.UnstructuredGrid):
    #     faces = custom_pv.cells.reshape(-1, custom_pv.cells[0] + 1)[:, 1:]

    return verts, faces

def write_off_file(filepath, verts, faces, colors=None):
    with open(filepath, 'w') as f:
        f.write('OFF\n')
        f.write('{} {} {}\n\n'.format(verts.shape[0], faces.shape[0], 0))
        
        for vert in verts:
            f.write('{} {} {}\n'.format(vert[0], vert[1], vert[2]))

        for idx, face in enumerate(faces):
            ''' https://stackoverflow.com/questions/2721521/fastest-way-to-generate-delimited-string-from-1d-numpy-array '''
            #generate an array with strings
            if colors is not None:
                face_arrstr = np.char.mod('%i', face)
                color_arrstr = ['%i'% c for c in colors[idx]]
                
                face_arrstr = list(face_arrstr) + list(color_arrstr)
            else:
                face_arrstr = np.char.mod('%i', face)
                
            face_str = " ".join(face_arrstr)
            
            f.write('{} {}\n'.format(len(face), face_str))

