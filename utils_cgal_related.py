def make_room_between_leaflet_boundaries(l1_pv, l2_pv, dist_threshold):
    left_commissure_idx = 1
    right_commissure_idx = 131
    hinge_idx = 449
    corner_vert_idxes = [left_commissure_idx,
                         right_commissure_idx,
                         hinge_idx]

    l1_verts, l1_faces = get_verts_faces_from_pyvista(l1_pv)
    border_vert_idxes = np.where(np.array(igl.is_border_vertex(l1_verts, l1_faces)))[0]
    border_vert_idxes_ordered = get_border_vert_idxes_in_order(l1_faces, left_commissure_idx, reverse=False)
    border_vert_idxes_ordered_list = split_border_vert_idxes_at_corners(border_vert_idxes_ordered, corner_vert_idxes)

    RH_idxes = border_vert_idxes_ordered_list[1]
    LH_idxes = np.flip(border_vert_idxes_ordered_list[2])

    min_length = min(len(RH_idxes), len(LH_idxes))
    RH_idxes = RH_idxes[5:min_length]
    LH_idxes = LH_idxes[5:min_length]

    # dists = np.linalg.norm(l1_pv.points[RH_idxes] - l2_pv.points[LH_idxes], axis=1)
    l1_RH_dists, _, l1_RH_closest_pts = igl.point_mesh_squared_distance(l1_pv.points[RH_idxes], *get_verts_faces_from_pyvista(l2_pv))
    l2_LH_dists, _, l2_LH_closest_pts = igl.point_mesh_squared_distance(l2_pv.points[LH_idxes], *get_verts_faces_from_pyvista(l1_pv))

    RH_idxes_too_close = RH_idxes[l1_RH_dists < dist_threshold/2]
    LH_idxes_too_close = LH_idxes[l2_LH_dists < dist_threshold/2]

    l1_pv_new = l1_pv.copy()
    l2_pv_new = l2_pv.copy()

    if RH_idxes_too_close.sum() > 0:
        l1_RH_mid_points = (l1_pv.points[RH_idxes_too_close] + l1_RH_closest_pts[l1_RH_dists < dist_threshold/2]) / 2
        l2_LH_mid_points = (l2_pv.points[LH_idxes_too_close] + l2_LH_closest_pts[l2_LH_dists < dist_threshold/2]) / 2

        l1_pv_new.points[RH_idxes_too_close] = l1_RH_mid_points + l1_pv.point_normals[RH_idxes_too_close] * dist_threshold/2
        l2_pv_new.points[LH_idxes_too_close] = l2_LH_mid_points + l2_pv.point_normals[LH_idxes_too_close] * dist_threshold / 2

    return l1_pv_new, l2_pv_new

def get_min_dist_verts(verts_choose_from, verts_want_closest_to, return_idx=False):
    min_dist_verts = []
    idx_list = []
    for vert in verts_want_closest_to:
        idx = np.argmin(np.linalg.norm(verts_choose_from - vert, axis=1))
        min_dist_verts.append(verts_choose_from[idx])
        idx_list.append(idx)
    min_dist_verts = np.array(min_dist_verts)

    if return_idx:
        return min_dist_verts, idx_list
    else:
        return min_dist_verts

