def get_border_vert_idxes_in_order(faces, start_idx, reverse=False):
    """
    start_idx: should be a corner vertex idx
    """
    if not reverse:
        init_axis = 1
        step_axis = 0
    else:
        init_axis = 0
        step_axis = 1

    exterior_edges = igl.exterior_edges(faces)

    idx_store = [start_idx]
    curr_idx = start_idx
    n_iter = 0
    next_idx = np.nan

    while (next_idx != start_idx) and n_iter<=1000:
        next_idx = exterior_edges[exterior_edges[:, init_axis] == curr_idx][:, step_axis][0]

        idx_store.append(next_idx)
        curr_idx = next_idx

        n_iter += 1

    if n_iter >= 1000:
        print('get_border_vert_idxes_in_order n_iter >= 1000')

    return np.array(idx_store)

def split_border_vert_idxes_at_corners(border_vert_idxes_ordered, corner_vert_idxes):
    split_idxes = []
    for corner_vert_idx in corner_vert_idxes:
        if (border_vert_idxes_ordered == corner_vert_idx).sum() > 1:
            continue
        else:
            split_idxes.append(np.where(border_vert_idxes_ordered == corner_vert_idx)[0][0])

    output_idxes = []
    idx_begin = 0
    for split_idx in split_idxes:
        output_idxes.append(border_vert_idxes_ordered[idx_begin:split_idx+1])
        idx_begin = split_idx
    output_idxes.append(border_vert_idxes_ordered[idx_begin:])

    return output_idxes

def split_quad_to_2_tri_mesh(faces):
    """ faces is a list of lists that are either tri and quad faces """
    tri_faces = []
    for face in faces:
        if len(face) == 4:
            tri_faces.append([face[0], face[1], face[2]])
            tri_faces.append([face[0], face[2], face[3]])
        else:
            tri_faces.append(face)

    return np.array(tri_faces)

